const wallet = new ethers.Wallet(privateKey, provider);

// Connect the wallet to a signer
const signer = wallet.connect(provider);

// Create a contract instance with the signer
const myTokenContract = new ethers.Contract(myTokenAddress, myTokenABI, signer);

// Example: Transfer tokens to learners
async function transferTokens(receiverAddress, amountToSend) {

  try {
    const tx = await myTokenContract.transferTokens(receiverAddress, amountToSend);
    await tx.wait();
    console.log('Tokens transferred successfully.');
  } catch (error) {
    console.error('Error transferring tokens:', error);
  }
}

